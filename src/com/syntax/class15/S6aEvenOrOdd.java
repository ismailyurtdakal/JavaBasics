package com.syntax.class15;

public class S6aEvenOrOdd {
//Task 2 Create a method that will take a number and prints whether the number is  even or odd
	void evenOrOdd(int a) {

		if (a % 2 == 0) {
			System.out.println("number " + a + " is even");
		} else {
			System.out.println("number " + a + " is odd");
		}
	}

}